public class Lingkaran {
    double hitungLuasLing(int r) {
        return (22 / 7 * r * r);
    }
    
    double hitungLuasLing(double r) {
        return (22 / 7 * r * r);
    }
    
    //hitung keliling
    double hitungKelilingLing(int r) {
        return (22 / 7 * 2 * r);
    }
    
    double hitungKelilingLing(double r) {
        return (22 / 7 * 2 * r);
    }
}
